import Foundation

// MARK: - UserSignIn
//회원가입 api 통신에 사용하는 body 구조
struct UserSignIn: Codable {
    let phoneNumber, fcMtoken, nick, birth: String
    let email: String
    let gender: Int

    enum CodingKeys: String, CodingKey {
        case phoneNumber
        case fcMtoken = "FCMtoken"
        case nick, birth, email, gender
    }
}

